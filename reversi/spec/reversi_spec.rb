require 'Reversi'

RSpec.describe "Reversi" do

  let(:game) { Reversi.new }

  it "can find legal moves on start board" do
    input =
'........
........
........
...BW...
...WB...
........
........
........
B'
    expected_output =
'........
........
....0...
...BW0..
..0WB...
...0....
........
........
B'
    expect(game.legal_moves(input)).to eq(expected_output)
  end

  it "can find legal moves acros multiple counters" do
    input =
'........
........
........
...BWWW.
...WB...
........
........
........
B'
    expected_output =
'........
........
....0.0.
...BWWW0
..0WB...
...0....
........
........
B'
    expect(game.legal_moves(input)).to eq(expected_output)
  end

  it "can find legal moves when at the border" do
    input =
'........
........
........
...BWWWW
...WB...
........
........
........
B'
    expected_output =
'........
........
....0.0.
...BWWWW
..0WB...
...0....
........
........
B'
    expect(game.legal_moves(input)).to eq(expected_output)
  end

  it "handles cases when no legal moves available" do
    input =
'WWWWWWWW
WWWWWWWW
WWWWWWWW
WWWWWWW.
WWWWWW..
WWWWWW.B
WWWWWWW.
WWWWWWWW
B'
    expected_output =
'WWWWWWWW
WWWWWWWW
WWWWWWWW
WWWWWWW.
WWWWWW..
WWWWWW.B
WWWWWWW.
WWWWWWWW
B'
    expect(game.legal_moves(input)).to eq(expected_output)
  end
end
